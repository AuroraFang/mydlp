-module(mydlp_acl).
-author("kerem@medra.com.tr").
-behaviour(gen_server).

-include("mydlp.hrl").

%% API
-export([start_link/0,
	q/4,
	stop/0]).

%% gen_server callbacks
-export([init/1,
	handle_call/3,
	handle_cast/2,
	handle_info/2,
	terminate/2,
	code_change/3]).

-include_lib("eunit/include/eunit.hrl").

-record(state, {}).

%%%%%%%%%%%%% MyDLP ACL API

q(_From, _Dest, _Data, Files) ->
	gen_server:call(?MODULE, {acl_q, {Files}}).

%%%%%%%%%%%%%% gen_server handles

handle_call({acl_q, {Files}}, From, State) ->
	Worker = self(),
	spawn_link(fun() ->
		F = fun(File) -> MT = mydlp_tc:get_mime(File#file.data), 
			erlang:display(binary_to_list(MT)),
			binary_to_list(mydlp_tc:get_text(File#file{mime_type=MT})) end,
		Texts = lists:map(F, Files),

		erlang:display(Texts),
		Result = pass,
		Worker ! {async_acl_q, Result, From}
	end),
	{noreply, State, 60000};

handle_call(stop, _From, State) ->
	{stop, normalStop, State};

handle_call(_Msg, _From, State) ->
	{noreply, State}.

handle_info({async_acl_q, Res, From}, State) ->
	gen_server:reply(From, Res),
	{noreply, State};

handle_info(_Info, State) ->
	{noreply, State}.

%%%%%%%%%%%%%%%% Implicit functions

start_link() ->
	case gen_server:start_link({local, ?MODULE}, ?MODULE, [], []) of
		{ok, Pid} -> {ok, Pid};
		{error, {already_started, Pid}} -> {ok, Pid}
	end.

stop() ->
	gen_server:call(?MODULE, stop).

init([]) ->
	{ok, #state{}}.

handle_cast(_Msg, State) ->
	{noreply, State}.

terminate(_Reason, _State) ->
	ok.

code_change(_OldVsn, State, _Extra) ->
	{ok, State}.

